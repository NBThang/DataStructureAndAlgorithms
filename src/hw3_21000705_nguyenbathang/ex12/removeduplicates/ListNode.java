package hw3_21000705_nguyenbathang.ex12.removeduplicates;

public class ListNode {
    int val;
    ListNode next;

    public  ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}